int		*ft_sortintd(int *s, int n)
{
	int i;
	int k;
	int b;

	k = 0;
	while (k < n)
	{
		i = 0;
		while (i < n)
		{
			if (s[k] > s[i])
			{
				b = s[i];
				s[i] = s[k];
				s[k] = b;
			}
			i++;
		}
		k++;
	}
	return (s);
}

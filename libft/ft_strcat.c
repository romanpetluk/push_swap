char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int k;

	k = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[k] != '\0')
	{
		s1[i] = s2[k];
		i++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
